def gen_financial():
    records = []
    for year_offset, fy_label in [(0, "FY2023"), (1, "FY2024")]:
        base_year = 2023 + year_offset  # FY2023: Apr 2023–Mar 2024
        for m_idx in range(12):
            # fiscal month: April(4) to March(3)
            cal_month = ((m_idx + 3) % 12) + 1  # 0→Apr(4), 11→Mar(3)
            cal_year = base_year if cal_month >= 4 else base_year + 1
            ym = f"{cal_year}-{cal_month:02d}"

            for seg in SEGMENTS_FIN:
                s_factor = SEASON[seg["name"]][cal_month - 1]
                yr_growth = 1 + seg["growth"] * year_offset
                noise = random.uniform(0.94, 1.06)

                revenue = round(seg["base_revenue"] * s_factor * yr_growth * noise)
                cost = round(revenue * (1 - seg["margin"]) * random.uniform(0.97, 1.03))
                op_profit = revenue - cost
                ordinary_profit = round(op_profit * random.uniform(0.92, 1.02))
                ebitda = round(op_profit * random.uniform(1.15, 1.30))

                # Budget: slightly optimistic target
                budget_rev = round(seg["base_revenue"] * s_factor * yr_growth * 1.03)
                budget_op = round(budget_rev * seg["margin"] * 1.02)

                staff = seg["staff"] + year_offset * random.randint(0, 3)

                cap = None
                gen_mwh = None
                util = None
                co2 = 0

                if seg["capacity_mw"] is not None:
                    cap = seg["capacity_mw"] + year_offset * random.randint(5, 25)
                if seg["gen_base"] is not None:
                    gen_mwh = round(seg["gen_base"] * s_factor * yr_growth * noise)
                if seg["util_base"] is not None:
                    util = round(seg["util_base"] * s_factor * random.uniform(0.96, 1.04), 1)
                if seg["co2_base"] > 0:
                    co2 = round(seg["co2_base"] * s_factor * yr_growth * noise)

                records.append({
                    "年月": ym,
                    "セグメント": seg["name"],
                    "売上高_百万円": revenue,
                    "売上原価_百万円": cost,
                    "営業利益_百万円": op_profit,
                    "経常利益_百万円": ordinary_profit,
                    "EBITDA_百万円": ebitda,
                    "予算_売上高_百万円": budget_rev,
                    "予算_営業利益_百万円": budget_op,
                    "設備容量_MW": cap,
                    "発電量_MWh": gen_mwh,
                    "設備利用率_pct": util,
                    "CO2削減量_t": co2,
                    "従業員数": staff,
                })
    return records


