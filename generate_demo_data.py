def gen_esg():
    records = []
    for year_offset in range(2):
        base_year = 2023 + year_offset
        for m_idx in range(12):
            cal_month = ((m_idx + 3) % 12) + 1
            cal_year = base_year if cal_month >= 4 else base_year + 1
            ym = f"{cal_year}-{cal_month:02d}"

            growth = 1 + 0.10 * year_offset
            s = SEASON["メガソーラー発電"][cal_month - 1]  # use solar season as proxy for CO2

            records.append({
                "年月": ym,
                "CO2削減量_t": round(350000 * s * growth * random.uniform(0.95, 1.05)),
                "再エネ供給量_MWh": round(750000 * s * growth * random.uniform(0.95, 1.05)),
                "RE100供給企業数": 58 + year_offset * 14 + m_idx,
                "グリーン電力証書発行_GWh": round(40 * s * growth * random.uniform(0.90, 1.10), 1),
                "カーボンクレジット販売_百万円": round(random.uniform(15, 35) * growth) if (m_idx + year_offset * 12) >= 8 else 0,
                "Scope1排出量_tCO2": round(1200 * random.uniform(0.90, 1.10)),
                "Scope2排出量_tCO2": round(800 * random.uniform(0.85, 1.15)),
                "水使用量_m3": round(45000 * random.uniform(0.90, 1.10)),
                "廃棄物量_t": round(120 * random.uniform(0.85, 1.15)),
                "労災件数": 0,
                "ヒヤリハット報告件数": random.randint(6, 15),
            })
    return records


